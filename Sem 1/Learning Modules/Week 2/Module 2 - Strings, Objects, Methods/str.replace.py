print("fred".replace("e", "2", 15))


# The replace() method in Python's string class takes only two arguments:

# str.replace(old, new[, count])
# The first argument old specifies the substring to be replaced,
# the second argument new specifies the new substring to replace 
# the old one with,and the optional third argument count specifies 
# the maximum numberof occurrences to replace.